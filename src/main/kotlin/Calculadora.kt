class Calculadora {

    companion object {
        fun soma(num1: Double, num2: Double): Double {
            return num1 + num2
        }

        fun sub(num1: Double, num2: Double): Double {
            return num1 - num2
        }

        fun mult(num1: Double, num2: Double): Double {
            return num1 * num2
        }

        fun div(num1: Double, num2: Double): Double {
            return num1 / num2
        }

        //---------------------------------------------------------------------------
        fun soma(num1: Int, num2: Int): Int {
            return num1 + num2
        }

        fun sub(num1: Int, num2: Int): Int {
            return num1 - num2
        }

        fun mult(num1: Int, num2: Int): Int {
            return num1 * num2
        }

        fun div(num1: Int, num2: Int): Int {
            return num1 / num2
        }
    }
}


